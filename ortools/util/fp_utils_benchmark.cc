// To scale numbers with pure power of two exponents the code relies on scalbn,
// the alternatives are ldexp and exp2. We benchmark here the alternatives to
// ensure scalbn is indeed faster.
void BM_fast_scalbn(benchmark::State& state) {
  const int length = 1024;
  std::vector<int> values;
  values.reserve(length);
  for (int i = 0; i < length; ++i) {
    values.push_back(i - 512);
  }

  for (auto s : state) {
    double total = 0;
    for (int i = 0; i < length; ++i) {
      total += fast_scalbn(1.0, values[i]);
    }
    CHECK_EQ(total, 0x1.ffffffffffffffp511);
  }
}
BENCHMARK(BM_fast_scalbn);

