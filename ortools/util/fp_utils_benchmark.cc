void BM_frexp(benchmark::State& state) {
  const int length = 1024;
  std::vector<double> values;
  values.reserve(length);
  for (int i = 0; i < length; ++i) {
    values.push_back(exp2(i - 512));
  }

  for (auto s : state) {
    int total = 0;
    for (int i = 0; i < length; ++i) {
      const double value = values[i];
      int exponent;
      if (value == 0.0) {
        exponent = FP_ILOGB0;
      } else {
        (void)frexp(values[i], &exponent);
      }
      total += exponent - 1;
    }
    CHECK_EQ(total, (length * (length - 1) / 2 - 512 * length));
  }
}
BENCHMARK(BM_frexp);

